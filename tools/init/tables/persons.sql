CREATE TABLE `PERSONS` (
    `PERSON_ID` INT UNSIGNED NOT NULL AUTO_INCREMENT,
    `STAKEHOLDER_ID` INT UNSIGNED NOT NULL,
    `PERSON_SALUTATION` VARCHAR(10),
    `PERSON_FIRSTNAME` VARCHAR(255),
    `PERSON_LASTNAME` VARCHAR(255) NOT NULL,
    `PERSON_GENDER` VARCHAR(255) NOT NULL,
    `PERSON_PHONE` VARCHAR(255),
    `PERSON_MOBILE` VARCHAR(255),
    `PERSON_MAIL` VARCHAR(255),
    `PERSON_BIRTHDAY` INT,
    `PERSON_STREET` VARCHAR(255),
    `PERSON_ZIP` VARCHAR(50),
    `PERSON_STATE` VARCHAR(255),
    `PERSON_CITY` VARCHAR(255),
    `PERSON_COUNTRY_ISO` VARCHAR(2) NOT NULL,
    `PERSON_CUSTOMER_FLAG` BOOLEAN NOT NULL,
    `PERSON_COMMENTS` TEXT,
    `PERSON_CREATED` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `PERSON_UPDATED` DATETIME ON UPDATE CURRENT_TIMESTAMP,
    `PERSON_CREATED_BY` INT NOT NULL,
    `PERSON_UPDATED_BY` INT,
    PRIMARY KEY (`PERSON_ID`),
    FOREIGN KEY (`STAKEHOLDER_ID`) REFERENCES `STAKEHOLDER` (`STAKEHOLDER_ID`) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE = INNODB COMMENT = 'table holding data about persons related to different entities / groups';